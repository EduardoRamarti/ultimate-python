import math

print(round(1.3))  # redondea al numero mas cercano del decimal
print(round(1.7))

print(abs(-77))  # regresa el valor absoluto
print(abs(55))

print(math.ceil(1.2))  # lo redondea al numero entero mas cercano hacia arriba
print(math.floor(1.2))  # lo redondea al numero entero mas cercano hacia abajo

print(math.isnan(23))  # regresa false por que comrpueba si NO es un numero

print(math.pow(10, 3))  # elevar la potencia a algo

print(math.sqrt(4))  # calcular raiz cuadrada
